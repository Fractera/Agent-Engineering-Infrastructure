// ФУНКЦИЯ УЗЛА «OUTPUT-CONNECTOR» — передаёт результат узлу ДРУГОЙ автоматизации: дверь наружу, в
// групповую автоматизацию. Принимает результат от ветки успеха (§2 закона) и отдаёт его тому, кто ждёт
// на той стороне; в одиночной автоматизации узел скрыт и не исполняется.
export function handToExternal(input: { result: unknown }): { handover: unknown } {
  return { handover: input.result };
}

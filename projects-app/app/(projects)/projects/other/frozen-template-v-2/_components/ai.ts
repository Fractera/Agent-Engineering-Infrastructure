import type { Passport } from "../_data/automation.schema";

// ПРОВАЙДЕР И МОДЕЛЬ ИИ — единственное место, где читается выбор из ядра, и единственный список
// доступных моделей. Меню показывает выбранное, настройки его меняют — оба берут отсюда.
//
// ГДЕ ХРАНИТСЯ ВЫБОР: в ПАСПОРТЕ автоматизации (`passport.ai`), а не в переменных окружения. Ключ
// провайдера — секрет и общий на весь проект (окружение); а вот КАКОЙ моделью работает ЭТА
// автоматизация — её собственное свойство, как тип или жизненный цикл. Две автоматизации на одном
// ключе вправе думать разными моделями.
//
// ЧТО ЗДЕСЬ НЕ ЖИВЁТ: ключи. Они идут через каталог `channels.ts` и дверь `api/env`, как у любого
// другого сервиса — второго механизма ввода секретов в продукте нет.

export type ProviderKey = "anthropic" | "openai";

export type Model = { id: string; label: string };

export type Provider = {
  key: ProviderKey;
  label: string;
  /** Ключ окружения — тот же механизм, что у каналов; описание лежит в каталоге `channels.ts`. */
  envKey: string;
};

// МОДЕЛИ НЕ ХАРДКОДЯТСЯ — они запрашиваются живьём у моста платформы через СОБСТВЕННУЮ дверь
// автоматизации `api/ai-models` (шаг 298, тем же источником, что пульт разработки 255.B1). Хардкод
// устаревает молча и врёт о том, чем провайдер думает СЕГОДНЯ (правка владельца 2026-07-23). Мост
// отдаёт список без ключа, поэтому прежний довод «живой список требует ключа» неверен. Здесь остаётся
// только НЕИЗМЕННОЕ: два провайдера и имя их ключа — сам список моделей приходит из двери.
export const PROVIDERS: Provider[] = [
  { key: "anthropic", label: "Anthropic", envKey: "ANTHROPIC_API_KEY" },
  { key: "openai", label: "OpenAI", envKey: "OPENAI_API_KEY" },
];

export const providerOf = (key: string): Provider => PROVIDERS.find((p) => p.key === key) ?? PROVIDERS[0];

/** Имена ключей провайдеров ИИ — их карточка живёт В КАРТОЧКЕ ИИ (по выбранному провайдеру), а НЕ в
 *  списке сервисов: ключ провайдера — свойство выбора в паспорте, а не объявления узла. Список сервисов
 *  их исключает по этому набору, иначе всплывала бы лишняя карточка невыбранного провайдера. */
export const PROVIDER_ENV_KEYS = new Set(PROVIDERS.map((p) => p.envKey));

/**
 * Что выбрано у ЭТОЙ автоматизации.
 *
 * Модель — просто строка из ядра: каталога моделей больше нет (живой список приходит из двери в момент
 * открытия настроек). Ярлык = сам id; человеческое имя рисует выпадающий список, когда получит живой
 * список от моста. Меню показывает выбранный id как есть — честнее, чем врать несуществующим ярлыком.
 */
export function aiOf(passport: Passport): { provider: Provider; model: Model } {
  const provider = providerOf(passport.ai.provider);
  return { provider, model: { id: passport.ai.model, label: passport.ai.model } };
}

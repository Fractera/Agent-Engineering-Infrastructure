import { existsSync } from "node:fs";
import { join } from "node:path";
import type { ReactNode } from "react";

// FAIL-SILENT ДЕВ-СЛОТ — серверная половина (закон устойчивости, шаг 298).
//
// Мягкий дев/админ-слой (`_shared-v2`) живёт СНАРУЖИ папки автоматизации. Продакшн от него не зависит
// НИКОГДА: нет `_shared-v2` — этот гейт рисует ничего, и автоматизация работает для конечного пользователя
// как ни в чём не бывало. Присутствие слоя проверяется на СЕРВЕРЕ на каждый запрос (страница динамическая),
// поэтому удаление `_shared-v2` на живом сервере убирает дев-кнопки без пересборки.
//
// Внутри, когда слой на месте, дети догружаются клиентским `dev-slot.client.tsx` (`next/dynamic` за
// error-boundary с null-фолбёком) — это второй, рантайм-слой той же защиты: модуль не загрузился/бросил →
// снова ничего.
const SHARED_V2 = join(process.cwd(), "app", "(projects)", "projects", "_shared-v2");

/** Показывает дев/админ-детей ТОЛЬКО когда мягкий слой присутствует; иначе — ничего. */
export function DevSlot({ children }: { children: ReactNode }) {
  if (!existsSync(SHARED_V2)) return null;
  return <>{children}</>;
}

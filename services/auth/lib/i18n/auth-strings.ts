// Static, build-time dictionary for the auth forms (login + register, incl. their
// modals / toasts / inline errors). ALL languages are baked into the bundle —
// there is NO per-user server request and NO runtime generation. The client
// component reads the BROWSER language (navigator.language) once and picks the
// matching entry; the words are therefore static, the only dynamic parts are the
// auth input fields themselves. Add a language = add one entry to STRINGS.
//
